/// AppSpacing — 4-point grid spacing constants.
///
/// Usage example:
///   Padding(padding: EdgeInsets.all(AppSpacing.md))
class AppSpacing {
  AppSpacing._();

  /// 4.0 — extra-small gap / icon padding.
  static const double xs = 4.0;

  /// 8.0 — small gap.
  static const double sm = 8.0;

  /// 12.0 — gutter / tight padding.
  static const double gutter = 12.0;

  /// 16.0 — medium / default padding.
  static const double md = 16.0;

  /// 20.0 — safe margin (horizontal screen edge padding).
  static const double safeMargin = 20.0;

  /// 24.0 — large gap.
  static const double lg = 24.0;

  /// 32.0 — extra-large gap.
  static const double xl = 32.0;

  /// 48.0 — double-extra-large gap.
  static const double xxl = 48.0;
}
